      <h2 id="lesson-cycle">Lesson Cycle</h2>
      <table style="width:100%;">
        <tr>
          <th>Padawan</th>
          <th>Jedi Knight</th>
          <th>Jedi Master</th>
        </tr>
        <tr>
          <td><ul>
              <li>The instructor completes some sections of the lesson cycle as evidenced in their lesson plans.</li>
              <li>The instructor makes lesson plans for some classes.</li>
              <li>Lessons are sometimes aligned with class learning goals.</li>
              <li>Assessment is used irregularly to determine students’ progress.</li>
          </ul></td>
          <td><ul>
              <li>The instructor completes all sections of the lesson cycle as evidenced in their lesson plans.</li>
              <li>The instructor makes lesson plans for every class.</li>
              <li>Lesson plans are aligned with class learning goals.</li>
              <li>Assessment is used regularly to determine students’ progress.</li>
          </ul></td>
          <td><ul>
              <li>The instructor completes all sections of the lesson cycle as evidenced in their lesson plans.</li>
              <li>The instructor makes lesson plans for every class and makes additional lesson plans for advanced students or for students who need extra help.</li>
              <li>Lesson plans are aligned with class learning goals. </li>
              <li>Formative and summative assessment is used everyday to determine students’ progress.</li>
          </ul></td>
        </tr>
      </table>
